public class CharacterEquivalents {
public static void main(String[] args) {
 // Define the characters
char[] characters = {'A', 'B', 'C', 'a', 'b', 'c', '0', '1', '2',
'$', '*', '+', '/', ' '};

// Display integer equivalents
for (char c : characters) {
System.out.println("Character: " + c + ", Integer Equivalent: " + (int) c);

    }
  }
}    